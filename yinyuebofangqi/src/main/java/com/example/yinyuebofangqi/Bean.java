package com.example.yinyuebofangqi;

public class Bean {
    private int music;
    private String name;

    public Bean(int music, String name) {
        this.music = music;
        this.name = name;
    }

    public int getMusic() {
        return music;
    }

    public void setMusic(int music) {
        this.music = music;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
