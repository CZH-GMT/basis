package com.example.duobuju;

public class Bean {
    private int iv;
    private String title;

    public Bean(int iv, String title) {
        this.iv = iv;
        this.title = title;
    }

    public int getIv() {
        return iv;
    }

    public void setIv(int iv) {
        this.iv = iv;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
